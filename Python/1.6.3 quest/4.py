day = int(input("Введите день рождения: "))
month = int(input("Введите месяц рождения: "))
year = int(input("Введите год рождения: "))

print(f"Дата рождения: {day:02d}.{month:02d}.{year}")
print(f"Дата рождения: {day:02d}/{month:02d}/{year}")
print(f"Дата рождения: {day:02d} {month:02d} {year}")
print(f"Дата рождения: {day:02d}-{month:02d}-{year}")